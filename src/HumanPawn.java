
public class HumanPawn extends Pawn {

	public HumanPawn(int posX, int posY, int walls, String name) {
		super(posX, posY, walls, name);
		
	}

	@Override
	public String getName() {
		
		return super.getName();
	}

	@Override
	public void setWalls(int set) {
		
		super.setWalls(set);
	}

	@Override
	public int getWalls() {
		
		return super.getWalls();
	}

	@Override
	public void setX(int set) {
		
		super.setX(set);
	}

	@Override
	public void setY(int set) {
		
		super.setY(set);
	}

	@Override
	public int[] getPosition() {
		
		return super.getPosition();
	}

	@Override
	public int getX() {
		
		return super.getX();
	}

	@Override
	public int getY() {

		return super.getY();
	}

	@Override
	public void moveX(int move) {

		super.moveX(move);
	}

	@Override
	public void moveY(int move) {

		super.moveY(move);
	}

}
